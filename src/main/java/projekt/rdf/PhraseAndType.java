package projekt.rdf;

public class PhraseAndType {

	private final String phrase;
	private final String type;
	
	
	public PhraseAndType(String aPhrase,String aType) {
		phrase = aPhrase;
		type = aType;
	}
	
	public String getPhrase() {
		return phrase;
	}


	public String getType() {
		return type;
	}

}
